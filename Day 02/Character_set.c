// Character Set
#include <stdio.h> // Input/output library for C programming language.

int main() {
    printf("Character Set:\n");
    printf("Uppercase Letters: A-Z\n"); // Print the uppercase letters in the character set.
    printf("Lowercase Letters: a-z\n"); // Print the lowercase letters in the character set.
    printf("Digits: 0-9\n"); // Print the digits in the character set.
    printf("Special Characters: !@#$%%^&*()-+\n"); // Print some special characters in the character set.
    return 0;
}