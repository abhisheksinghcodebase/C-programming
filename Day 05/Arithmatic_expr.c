// Arithmatic expression in C

// Operands and operators in C
#include <stdio.h>

int main() {
    int a = 10, b = 5;
    int sum = a + b;
    int difference = a - b;
    int product = a * b;
    int quotient = a / b;
    int remainder = a % b;

    printf("Sum: %d\n", sum); // This will print the result of a + b
    printf("Difference: %d\n", difference); // This will print the result of a - b
    printf("Product: %d\n", product); // This will print the result of a * b
    printf("Quotient: %d\n", quotient); // This will print the result of a / b
    printf("Remainder: %d\n", remainder); // This will print the result of a % b

    return 0;
}