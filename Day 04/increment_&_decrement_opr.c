// Increment and Decrement Operators

#include <stdio.h> // Include the standard input-output library

int main() {
    int a = 10;

    printf("Initial value of a: %d\n", a);

    a++; // Increment a by 1
    printf("After a++: %d\n", a);

    a--; // Decrement a by 1
    printf("After a--: %d\n", a);

    return 0;
}