// Writing character in C programming

#include <stdio.h>

int main(){
    // character value writing
    char ch = 'A'; // Assigning a character value to variable ch
    printf("The character value is: %c\n", ch); // Displaying the character value

    // string value writing
    char str[] = "Hello, World!"; // Assigning a string value to variable str
    printf("The string value is: %s\n", str); // Displaying the string value

    return 0;
}